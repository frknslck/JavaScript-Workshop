document.write("<table border='1'cellspacing=0><tr>");
for (let row = 1; row <= 10; row++) {
    document.write("<td>");
    for (let col = 1; col <= 10; col++) {
        carpim = row * col;
        document.write(`${row}x${col} = ${carpim} <br> <hr>`);
    }
    document.write("</td>");
}
document.write("</tr></table>");